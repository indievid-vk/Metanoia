import fs from 'fs';

const filePath = 'src/data/catechesisQuestions.json';
let data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

for (const group of data) {
  for (const item of group.items) {
    if (item.q === "В чем отличие человеческой души от души животных? Какие три силы есть в душе человека?") {
      item.a = "Душа человека состоит из трех сил:\n- Сила словесности (ум, дух) - разумная способность, дающая человеку возможность богообщения и отличающая его от животных.\n- Сила желания (вожделевательная) — эмоциональная энергия души, которая у человека должна быть подчинена разуму.\n- Сила энергии (раздражительная) — воля и действие, способность выбирать и реализовывать намерения, также требующая руководства разума.\nТолько человек обладает силой словесности, благодаря которой способен уподобляться Богу и причастен к вечности.";
    }
  }
}

fs.writeFileSync(filePath, JSON.stringify(data, null, 2));
